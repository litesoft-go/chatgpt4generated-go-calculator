package calculator

// Add two integers
//
//goland:noinspection ALL
func Add(a int, b int) int {
	return a + b
}

// Subtract two integers
//
//goland:noinspection ALL
func Subtract(a int, b int) int {
	return a - b
}

// Multiply two integers
//
//goland:noinspection ALL
func Multiply(a int, b int) int {
	return a * b
}

// Divide two integers
//
//goland:noinspection ALL
func Divide(a int, b int) int {
	return a / b
}
